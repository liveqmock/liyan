package com.liyan.common.exception;

public class LogException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -5849650901699234233L;

	public LogException() {
		super();
	}

	public LogException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public LogException(String arg0) {
		super(arg0);
	}

	public LogException(Throwable arg0) {
		super(arg0);
	}
	

}
